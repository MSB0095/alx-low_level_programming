#include "3-calc.h"

/**
 * op_add - a function that adds 2 integers
 * @a: first int
 * @b: second int
 * Return: a+b
*/

int op_add(int a, int b)
{
	return(a + b);
}

/**
 * op_sub - a function that substracts 2 integers
 * @a: first int
 * @b: second int
 * Return: a-b
*/

int op_sub(int a, int b)
{
	return(a - b);
}

/**
 * op_mul - a function that multiplies 2 integers
 * @a: first int
 * @b: second int
 * Return: a*b
*/

int op_mul(int a, int b)
{
	return(a * b);
}

/**
 * op_div - a function that divides 2 integers
 * @a: first int
 * @b: second int
 * Return: a/b
*/

int op_div(int a, int b)
{
	return(a / b);
}

/**
 * op_mod - a function that modulo 2 integers
 * @a: first int
 * @b: second int
 * Return: a%b
*/

int op_mod(int a, int b)
{
	return(a % b);
}

